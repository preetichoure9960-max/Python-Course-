a=int(input("Enter a number"))
b=int(input("Enter a number"))
#arithmetic operators
print(a + b) 
print(a - b)  
print(a * b)  
print(a / b)  
print(a ** b)  
print(a % b) 

#comparison operators
print(a == b)  
print(a != b)  
print(a > b)  
print(a < b)  
print(a >= b)  
print(a <= b) 

#logical operators
print(a > b and a == ("enter a number"))  
print(a > b or a == ("enter a number"))  
print(not a > b)
